// Función para obtener preguntas por categoría
export function getPreguntasPorCategoria(categoria) {
    return preguntas.filter(p => p.categoria === categoria);
}

// Función para mezclar preguntas
export function mezclarPreguntas(preguntasArray) {
    return [...preguntasArray].sort(() => 0.5 - Math.random());
}

// Función para obtener preguntas mixtas
export function getPreguntasMixtas() {
    const todasLasPreguntas = [...preguntas];
    return mezclarPreguntas(todasLasPreguntas).slice(0, 15);
}
